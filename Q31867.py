"""
문제
민구는 자릿수가 
$N$인 양의 정수 
$K$로 홀짝 놀이를 하려고 한다.

홀짝 놀이의 과정은 다음과 같다.

각 자릿수에 대해서 해당 숫자가 짝수인지 홀수인지를 확인한다. 숫자 
$0$은 짝수이다.
모든 자릿수에 대해서 홀짝 판별이 완료되면 전체 짝수의 개수와 홀수의 개수를 비교한다.
짝수의 개수가 홀수의 개수보다 더 많으면 주어진 수는 짝짝수이다. 홀수의 개수가 짝수의 개수보다 더 많으면 홀홀수이다.
숫자가 너무 길어서 개수를 세는 것에 어려움을 겪는 민구를 위해 홀짝 놀이를 도와주는 프로그램을 작성하자!

입력
첫 번째 줄에 자릿수 
$N$이 주어진다. 
$(1 \le N \le 10^6)$ 

두 번째 줄에 자연수 
$K$가 주어진다. 
$(1 \le K)$ 

양의 정수 
$K$가 
$0$으로 시작하는 경우는 주어지지 않는다.

출력
첫 번째 줄에 주어진 자연수가 짝짝수면 
$0$, 홀홀수면 
$1$, 둘 다 아니라면 
$-1$을 출력한다.
"""

N = int(input())
K = input()
odd, even = 0, 0

for n in range(N):
    if int(K[n]) % 2 == 0:
        even += 1
    else:
        odd += 1

if even > odd:
    print(0)
elif even < odd:
    print(1)
else:
    print(-1)